// ============================================================
// 1. 简单赋值语句
// ============================================================
x = 3 + 4 * 5;
y = (a + b) * (c - d) / e;

// ============================================================
// 2. if 语句（无 else）
// ============================================================
if (x > 0) y = 1;

// ============================================================
// 3. if-else 语句
// ============================================================
if (x > 0)
    y = 1;
else
    y = 0;

// ============================================================
// 4. 代码块
// ============================================================
{
    a = 10;
    b = 20;
    c = a + b;
}

// ============================================================
// 5. 嵌套 if 与代码块
// ============================================================
if (a > b) {
    result = a - b;
} else {
    result = b - a;
}

// ============================================================
// 6. 复杂表达式（多运算符、括号嵌套）
// ============================================================
total = (price * quantity) + (tax * amount) - discount;
area = 3.14 * radius * radius;

// ============================================================
// 7. 一元运算符
// ============================================================
neg = -value;
flag = !condition;
bitmask = ~flags;

// ============================================================
// 8. 深度嵌套的代码块
// ============================================================
{
    {
        {
            x = 1;
        }
    }
}

// ============================================================
// 9. 多个语句序列
// ============================================================
a = 1;
b = 2;
c = 3;
result = a + b * c;
quotient = a / b;
remainder = a % b;

// ============================================================
// 10. 嵌套 if 与复杂条件
// ============================================================
if (x + y > z * 2) {
    if (a == b) {
        answer = 42;
    } else {
        answer = 0;
    }
}

// ============================================================
// 11. 比较运算符
// ============================================================
if (a == b) x = 1;
if (a != b) x = 2;
if (a < b) x = 3;
if (a > b) x = 4;
if (a <= b) x = 5;
if (a >= b) x = 6;

// ============================================================
// 12. 逻辑运算符
// ============================================================
if (a > 0 && b > 0) positive = 1;
if (a > 0 || b > 0) any_positive = 1;

// ============================================================
// 13. 混合优先级
// ============================================================
result = a + b * c - d / e;
complex = (a + b) * (c - d) % (e + f);

// ============================================================
// 14. 嵌套 if-else
// ============================================================
if (x > 100) {
    if (y > 50) {
        z = 1;
    } else {
        z = 2;
    }
} else {
    if (x > 50) {
        z = 3;
    } else {
        z = 4;
    }
}

